void main(){
  num mark=50;

  if(mark>=90 && mark<=100){
    print("A+");
  }else if(mark>=80 && mark<=89){
    print("A");
  }else if(mark >=70 && mark<=79){
    print("A+");
  }else if(mark>=60 && mark<=69){
    print("B");
  }else if(mark >50 && mark<=59){
    print("C+");
  }else{
    print("C");
  }
}